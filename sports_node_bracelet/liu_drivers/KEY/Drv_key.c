unsigned char zero[1][16]=
{
	0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,
};
	//按键初始化函数
void KEY_Init(void)
{
	
	GPIO_InitTypeDef  GPIO_InitStructure;

  RCC_AHB1PeriphClockCmd(RCC_AHB1Periph_GPIOB, ENABLE);//使能GPIOA,GPIOE时钟
 
  GPIO_InitStructure.GPIO_Pin = GPIO_Pin_12|GPIO_Pin_13|GPIO_Pin_14|GPIO_Pin_15; //KEY0 KEY1 KEY2对应引脚
  GPIO_InitStructure.GPIO_Mode = GPIO_Mode_IN;//普通输入模式
  GPIO_InitStructure.GPIO_Speed = GPIO_Speed_100MHz;//100M
  GPIO_InitStructure.GPIO_PuPd = GPIO_PuPd_UP;//上拉
  GPIO_Init(GPIOB, &GPIO_InitStructure);//初始化GPIOE2,3,4
	
	 
  GPIO_InitStructure.GPIO_Pin = GPIO_Pin_0;//WK_UP对应引脚PA0
  GPIO_InitStructure.GPIO_PuPd = GPIO_PuPd_DOWN ;//下拉
  GPIO_Init(GPIOA, &GPIO_InitStructure);//初始化GPIOA0
 
} 
//按键处理函数
//返回按键值
//mode:0,不支持连续按;1,支持连续按;
//0，没有任何按键按下
//1，KEY0按下
//2，KEY1按下
//3，KEY2按下 
//4，WKUP按下 WK_UP
//注意此函数有响应优先级,KEY0>KEY1>KEY2>WK_UP!!
u8 KEY_Scan(u8 mode)
{	 
	  static u8 key_up=1;//按键按松开标志
	  if(key_up&&(KEY4==0||KEY1==0||KEY2==0||KEY3==0))
	{  
		delay_ms(10);//去抖动 
		key_up=0;
		if(KEY1==0) 
			
		return 1;
		else if(KEY2==0)
			
		return 2;
		else if(KEY3==0)
			
		return 3;
		else if(KEY4==0)
			
		return 4;
	}else if(KEY4==1&&KEY1==1&&KEY2==1&&KEY3==1)key_up=1; 	    
 	return 0;// 无按键按下
}

void Key_Run(void)
{
	u8 key;           //保存键值
  key=KEY_Scan(0);		//得到键值
	if(key)
	{						   
		switch(key)
		{				
			case KEY1_PRES:	//控制LED0翻转
				if( 2 == flag.display)
				{
					switch(flag.index)        
					{			
						case 1:
							flag.display = 3;
							break;
						case 2:
							flag.tBase += 0.1;
							break;
						case 3:
							motion.steps = 0;	
							motion.dis = 0;	
						
  						OLED_DrawBMP(8*(7),0,8*8,2,zero[0]);
							break;
						case 4:
							break;
					}
				}
				break;
			case KEY2_PRES:	//控制LED1翻转
				flag.display = 1;
				OLED_Clear();		
				break;
			case KEY3_PRES:	//同时控制LED0,LED1翻转 
				if( 1 == flag.display)		
				{
					flag.display = 2;
					OLED_Clear();
				}
				if( 2 == flag.display)
						{
							switch(flag.index)        
							{			
								case 1:
									break;
								case 2:
									flag.tBase -= 0.1;
									break;
								case 3:
									break;
								case 4:
									break;
							}
						}
					break;
			case KEY4_PRES:	//控制蜂鸣器
				flag.index = (flag.index<4)? flag.index + 1 : 1 ;
				break;
		}
	}
}
