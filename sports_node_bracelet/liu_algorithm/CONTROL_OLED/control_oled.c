void oled_mode_display(void)
{
	if( 1 == flag.display )
	{
		oled_display1();
		switch(flag.index)
		{
			case 1: oled_display1_mode(1);
				break;
			case 2:	oled_display1_mode(2);
				break;
			case 3:	oled_display1_mode(3);
				break;
		}
	}			
	if( 2 == flag.display)
	{
		switch(flag.index)
		{			
			case 1: oled_display2_mode(1);
				break;
			case 2:	oled_display2_mode(2);
				break;
			case 3:	oled_display2_mode(3);
				break;
		}
	}
	if( 3 == flag.display)
	{
		OLED_Waveform_display();
	}
}

